        *(feature_data + 1) = imgsensor_info.min_shutter;
        break;
    case SENSOR_FEATURE_GET_OFFSET_TO_START_OF_EXPOSURE:
        *(MUINT32 *)(uintptr_t)(*(feature_data + 1)) = -17440000;
        break;
    case SENSOR_FEATURE_GET_SEAMLESS_SCENARIOS:
